package org.lab11.labwork5;

/**
 * The interface Iterator.
 */
public interface Iterator {
    /**
     * Method hasNext()  check encapsulated last object.
     *
     * @return the boolean
     */
    public boolean hasNext();

    /**
     * Method next() gives object from iterator.
     *
     * @return the object
     */
    public Object next();
}
